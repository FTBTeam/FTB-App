export type EndpointDefinition<Input, Output> = {
  input: Input;
  output: Output;
};

/**
 * Helper method to create type-safe endpoint definitions.
 */
function io<Input, Output>(): EndpointDefinition<Input, Output> {
  return {
    input: {} as Input,
    output: {} as Output
  };
}

